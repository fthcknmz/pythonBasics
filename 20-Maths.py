# built-in math functions
x = min(5,20,50) 
y = max(5,20,50)
print("min value:",x,"max value:",y,"\n")

x = abs(-7.85)
print("absolute value:",x,"\n")

x = pow(2,5)
print("2 to the power of 5:",x,"\n")

# math module
import math  # we can extend math functions
x = math.sqrt(64)
print("square of 64:",x,"\n")

x = math.ceil(1.7) # rounds a number upward to its nearest integer
print(x)
x = math.floor(1.7) # rounds a number downward to its nearest integer
print(x)
print()
x = math.pi
print("pi: ",x)